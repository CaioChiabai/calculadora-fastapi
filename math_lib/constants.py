"""
Constantes matemáticas úteis
"""

import math

# Constantes matemáticas fundamentais
PI = math.pi  # π (pi) ≈ 3.141592653589793
E = math.e    # e (número de Euler) ≈ 2.718281828459045

# Proporção áurea
GOLDEN_RATIO = (1 + math.sqrt(5)) / 2  # φ (phi) ≈ 1.618033988749895

# Outras constantes úteis
TAU = 2 * PI  # τ (tau) = 2π ≈ 6.283185307179586
SQRT_2 = math.sqrt(2)  # √2 ≈ 1.4142135623730951
SQRT_3 = math.sqrt(3)  # √3 ≈ 1.7320508075688772

# Constantes de conversão
DEGREES_TO_RADIANS = PI / 180
RADIANS_TO_DEGREES = 180 / PI

# Constantes físicas comuns (valores aproximados)
SPEED_OF_LIGHT = 299792458  # m/s
GRAVITY = 9.81  # m/s² (aceleração da gravidade na Terra)
AVOGADRO_NUMBER = 6.02214076e23  # mol⁻¹
